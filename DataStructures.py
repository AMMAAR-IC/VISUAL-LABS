# Demonstrates: Lists, Dictionaries, Sets, Tuples
 
# List
fruits = ["apple", "banana", "cherry"]
fruits.append("mango")
print("List:", fruits)

# Dictionary
student = {"name": "Ammaar", "age": 20, "course": "AI"}
print("Student Name:", student["name"])

# Set
unique_numbers = {1, 2, 2, 3, 4}
print("Set (no duplicates):", unique_numbers)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)
